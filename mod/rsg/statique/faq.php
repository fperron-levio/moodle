<div id="certificat"></div></br>
<h3>Évaluation et certificat</h3>
<hr />
<div id="certificatQ01"></div>
<h5 class="static-page-title">La formation est-elle évaluée?</h5>
<p>Oui. La formation consiste en une autoévaluation des apprentissages réalisés.</p>
<hr />
<div id="certificatQ02"></div>
<h5 class="static-page-title">Qu'est-ce qu'une unité d'éducation continue (UEC)?</h5>
<p>Une unité d'éducation continue (UEC) est une mesure qui garantit la valeur et le respect des normes de qualité d'une formation. Ces normes sont établies par la Société de formation et d'éducation continue (SOFEDUC).</p>
<hr />
<div id="certificatQ03"></div>
<h5 class="static-page-title">Qu'est-ce que la SOFEDUC?</h5>
<p>La SOFEDUC est une Société dont le mandat consiste à accréditer des organisations aptes à émettre des unités d'éducation continue (UEC) répondant à des normes de qualité de haut niveau.</p>
<hr />
<div id="certificatQ04"></div>
<h5 class="static-page-title">À quoi servent les unités d'éducation continue (UEC)?</h5>
<p>Plusieurs ordres, associations, regroupements professionnels, entreprises privées, publiques et parapubliques reconnaissent les UEC comme une marque d'excellence qui valide les processus pédagogiques et administratifs de la formation. De plus en plus de professionnels sont tenus d'obtenir des UEC pour justifier leurs heures de formation obligatoire.</p>
<hr />
<div id="certificatQ05"></div>
<h5 class="static-page-title">Est-ce que je reçois un certificat?</h5>
<p>Un certificat des heures de perfectionnement réalisées est disponible dans la page <a href="/mod/rsg/monparcours/"><u>Mon parcours</u></a> lorsque la capsule est terminée, incluant l'autoévaluation. Vous devez imprimer vous-même votre certificat; il ne vous sera pas envoyé par la poste.</p>

<hr />
<div id="certificatQ06"></div>
<h5 class="static-page-title">Pourquoi m’est-il impossible d’accéder à mon certificat même si j’ai terminé mon autoévaluation?</h5>
<p>Lorsque vous avez terminé votre autoévaluation, il est important de soumettre vos réponses pour obtenir votre certificat.</p>

