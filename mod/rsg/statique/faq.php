<div id="profil"></div>
<h3>Inscription, connexion et profil</h3>
<hr />
<div id="profilQ01"></div>
<h5 class="static-page-title">Qui peut s'inscrire à <i>RSG en ligne</i>?</h5>
<p>Seules les RSG représentées peuvent actuellement s'inscrire. L'information concernant l'accès à d'autres clientèles sera diffusée ultérieurement.</p>
<hr />
<div id="profilQ02"></div>
<h5 class="static-page-title">Qu'est-ce qu'une RSG représentée?</h5>
<p>Une RSG représentée fait partie de l'une des associations suivantes :
  <ul>
      <li>Fédération des intervenantes en petite enfance du Québec – Centrale des syndicats du Québec (FIPEQ-CSQ);</li>
      <li>Fédération de la santé et des services sociaux – Confédération des syndicats nationaux (FSSS-CSN);</li>
      <li>Regroupement des travailleuses et travailleurs autonomes des centres de la petite enfance (RTTACPE);</li>
      <li>Association des éducatrices et éducateurs en milieu familial du Québec (AEMFQ).</li>
  </ul>
</p>
<hr />
<div id="profilQ03"></div>
<h5 class="static-page-title">Y a-t-il des frais d'inscription?</h5>
<p>Non. Il n’y pas de frais pour s'inscrire ni pour utiliser la plateforme <i>RSG en ligne</i>.</p>
<hr />
<div id="profilQ04"></div>
<h5 class="static-page-title">Combien coûte une capsule?</h5>
<p>Les capsules de formation sont offertes gratuitement.</p>
<hr />
<div id="profilQ05"></div>
<h5 class="static-page-title">Comment puis-je m'inscrire à <i>RSG en ligne</i>?</h5>
<p>Sur la page de connexion, cliquez sur le bouton S'inscrire dans la section Première connexion? et remplissez le formulaire pour créer votre profil.</p>
<hr />
<div id="profilQ06"></div>
<h5 class="static-page-title">Dois-je me réinscrire à chaque année?</h5>
<p>Non. Une fois inscrite à <i>RSG en ligne</i>, vous conservez vos droits d'accès à la plateforme tant que vous demeurez une RSG représentée.</p>
<hr />
<div id="profilQ07"></div>
<h5 class="static-page-title">Comment puis-je modifier mes renseignements personnels?</h5>
<p>Dans la page <a href="/mod/rsg/monprofil/"><u>Mon profil</u></a>, vous pouvez modifier votre numéro de téléphone, votre mot de passe et votre adresse courriel. Pour modifier un autre élément, communiquez avec le soutien technique à partir de la page <a href="/mod/rsg/contact/"><u>Nous joindre</u></a>.</p>
<hr />
<div id="profilQ08"></div>
<h5 class="static-page-title">Pourquoi est-ce que je ne parviens pas à m'inscrire?</h5>
<p>Vous êtes possiblement dans l’une des situations suivantes :
<ol>
    <li>Vous avez fait une erreur en inscrivant votre numéro d'identification ou une autre information;</li>
    <li>Vous n'êtes pas une RSG représentée (pour en savoir plus, consultez la question Qu'est-ce qu'une RSG représentée?, plus haut dans cette section);</li>
    <li>Vous êtes une RSG représentée depuis peu et votre numéro d'identification n'apparait pas encore dans notre base de données. Vous pourrez vous inscrire et commencer une capsule de formation dans un délai maximal de 28 jours;</li>
    <li>Si aucune de ces situations ne s’applique, communiquez avec le soutien technique en utilisant le formulaire disponible dans la page <a href="/mod/rsg/contact/"><u>Nous joindre</u></a>.</li>
</ol>
</p>
<hr />
<div id="profilQ09"></div>
<h5 class="static-page-title">J’ai réussi à m’inscrire, mais je ne reçois pas le courriel de confirmation de mon inscription. Pourquoi?</h5>
<p>Le courriel de confirmation se trouve probablement dans votre boite de courrier indésirable. Dans ce cas, veuillez le déplacer dans votre boite de réception pour recevoir dorénavant les courriels provenant de <i>RSG en ligne</i>.</p>

<p>Si le courriel de confirmation n’est pas dans la boite de courrier indésirable, communiquez avec le soutien technique en utilisant le formulaire disponible dans la page <a href="/mod/rsg/contact/"><u>Nous joindre</u></a>.</p>
<hr />
<div id="profilQ10"></div>
<h5 class="static-page-title">Pourquoi est-ce que je n'arrive pas à me connecter, alors que je suis déjà inscrite?</h5>
<p>Vous êtes possiblement dans l'une des situations suivantes  :
<ol>
    <li>Vous avez fait une erreur en inscrivant votre numéro d'identification ou votre mot de passe;</li>
    <li>Votre statut a changé :
      <ul>
          <li>Vous n'êtes plus une RSG représentée. Seules les RSG représentées peuvent actuellement accéder à <i>RSG en ligne</i>;</li>
          <li>Pour en savoir plus, consultez la question Qu’est-ce qu’une RSG représentée?, plus haut dans cette section.</li>
      </ul>
    </li>
    <li>Si aucune de ces situations ne s'applique, communiquez avec le soutien technique en utilisant le formulaire disponible dans la page <a href="/mod/rsg/contact/"><u>Nous joindre</u></a>.</li>
</ol>
</p>

