<div id="capsules"></div></br>
<h3>Fonctionnement des capsules</h3>
<hr />
<div id="capsulesQ01"></div>
<h5 class="static-page-title">Comment en savoir plus sur le contenu d'une capsule?</h5>
<p>Dans le catalogue de capsules, cliquez sur le titre qui figure sur la vignette de la capsule choisie.</p>
<hr />
<div id="capsulesQ02"></div>
<h5 class="static-page-title">Comment la durée d'une capsule est-elle déterminée?</h5>
<p>La durée d'une capsule est déterminée par le temps moyen requis pour lire tous les textes, réaliser toutes les activités et remplir l'autoévaluation.</p>
<hr />
<div id="capsulesQ03"></div>
<h5 class="static-page-title">Combien de temps puis-je prendre pour faire une capsule?</h5>
<p>Vous pouvez prendre tout le temps nécessaire pour terminer une capsule. Chacune est structurée pour vous permettre d'apprendre à votre rythme. Vous pouvez donc revoir une théorie ou reprendre une activité si vous le souhaitez.</p>
<hr />
<div id="capsulesQ04"></div>
<h5 class="static-page-title">Est-ce que je peux faire reconnaitre un plus grand nombre d'heures que celui prévu pour une capsule?</h5>
<p>Non. Le temps reconnu est celui qui est inscrit sur la vignette de la capsule.</p>
<hr />
<div id="capsulesQ05"></div>
<h5 class="static-page-title">Puis-je arrêter ma formation et la reprendre à un autre moment?</h5>
<p>Oui, mais vous devez <strong>obligatoirement</strong> cliquer sur Quitter pour que vos données soient sauvegardées automatiquement. Vous pourrez reprendre là où vous vous êtes arrêtée par la suite.</p>
<hr />
<div id="capsulesQ06"></div>
<h5 class="static-page-title">Puis-je changer mes réponses ou refaire un exercice?</h5>
<p>Oui.</p>
<hr />
<div id="capsulesQ07"></div>
<h5 class="static-page-title">Puis-je récupérer un texte ou un document dans une capsule?</h5>
<p>Oui. Le Centre d'impression vous permet d'imprimer les documents que vous souhaitez conserver, et ce, dans toutes les capsules.</p>
<hr />
<div id="capsulesQ08"></div>
<h5 class="static-page-title">Puis-je refaire une capsule que j'ai bien aimée?</h5>
<p>Oui. Vous pouvez refaire une capsule. Cependant, si vous avez terminé et soumis votre autoévaluation, vous pouvez relire celle-ci mais non la reprendre.</p>
<hr />
<div id="capsulesQ09"></div>
<h5 class="static-page-title">Comment puis-je obtenir de l'aide si j'ai de la difficulté dans une capsule?</h5>
<p>Si vous avez besoin d'aide en ce qui concerne :
<ul>
    <li>Le fonctionnement d’une activité :
    <ul>
        <li>Consultez l'Aide en ligne. Le bouton est situé dans le haut de la page de chaque capsule;</li>
        <li>Visionnez la vidéo « Explorer une capsule de perfectionnement », qui se trouve sur la page d'accueil de la plateforme.</li>
    </ul>
    </li>
    <li>Une question pédagogique :
    <ul>
        <li>Veuillez vous adresser à votre bureau coordonnateur.</li>
    </ul>
    </li>
</ul>
</p>

<hr />
<div id="capsulesQ10"></div>
<h5 class="static-page-title">Puis-je faire une recherche dans le catalogue de capsules?</h5>
<p>Oui. Pour effectuer une recherche, inscrivez un ou plusieurs mots clés dans le champ Rechercher. Il faut éviter les longues phrases pour permettre au système de faire une recherche pertinente.</p>

