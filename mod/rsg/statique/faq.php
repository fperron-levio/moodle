<div id="identification"></div></br>
<h3>Numéro d'identification et mot de passe</h3>
<hr />
<div id="identificationQ01"></div>
<h5 class="static-page-title">Qu'est-ce que le numéro d'identification?</h5>
<p>Composé d'une série de chiffres, le numéro d'identification correspond au numéro inscrit sur le bordereau de paiement de la subvention transmis par votre bureau coordonnateur.</p>
<hr />
<div id="identificationQ02"></div>
<h5 class="static-page-title">Comment puis-je me procurer mon numéro d'identification?</h5>
<p>Votre numéro d'identification est inscrit sur le bordereau de paiement de la subvention transmis par votre bureau coordonnateur.</p>
<hr />
<div id="identificationQ03"></div>
<h5 class="static-page-title">Je ne reçois pas de bordereau de paiement; comment puis-je me procurer mon numéro d'identification?</h5>
<p>La plateforme n'est pas accessible aux RSG non subventionnées actuellement, celles-ci n'étant pas représentées. L'information concernant l'accès à la plateforme à d'autres clientèles sera diffusée ultérieurement.</p>
<hr />
<div id="identificationQ04"></div>
<h5 class="static-page-title">J'ai perdu mon numéro d'identification. Comment puis-je le récupérer?</h5>
<p>Vous pouvez le récupérer sur la page de connexion, dans la section Déjà inscrite? Cliquez sur le lien Numéro d'identification.</p>
<hr />
<div id="identificationQ05"></div>
<h5 class="static-page-title">J'ai perdu mon mot de passe. Comment puis-je le récupérer?</h5>
<p>Vous pouvez le récupérer sur la page de connexion, dans la section Déjà inscrite? Cliquez sur le lien Mot de passe.</p>
<hr />
<div id="identificationQ06"></div>
<h5 class="static-page-title">Où puis-je modifier mon mot de passe?</h5>
<p>Vous pouvez modifier votre mot de passe dans la page <a href="/mod/rsg/monprofil/"><u>Mon profil</u></a>, sous l'onglet Mon mot de passe.</p>
<hr />
<div id="identificationQ07"></div>
<h5 class="static-page-title">Qu'est-ce que l'ancienne adresse courriel?</h5>
<p>Il s'agit de l'adresse courriel que vous avez utilisée sur la page de connexion de la plateforme pour vous inscrire.</p>


