<div id="soutien"></div></br>
<h3>Problème technique</h3>
<hr />
<div id="soutienQ01"></div>
<h5 class="static-page-title">Que puis-je faire si je rencontre un problème technique?</h5>
<p>Si vous éprouvez une difficulté technique, vous pouvez :
<ol>
    <li>Utiliser un appareil recommandé (consultez la page <a href="/mod/rsg/requis/"><u>Exigences techniques</u></a>);</li>
    <li>Utiliser Google Chrome (navigateur recommandé);</li>
    <li>Visionner la vidéo portant sur la plateforme;</li>
    <li>Visionner la vidéo portant sur une capsule de perfectionnement;</li>
    <li>Consulter l’Aide en ligne;</li>
    <li>Consulter la présente section, Problème technique.</li>
</ol>
</p>
<hr />
<div id="soutienQ02"></div>
<h5 class="static-page-title">Je rencontre l'une des difficultés suivantes :
<ul>
<li>Il ne se passe rien lorsque je clique sur les boutons d’une capsule.</li>
<li>Les vidéos ne démarrent pas ou fonctionnent mal.</li>
</ul></h5>

<h5 class="static-page-title">Que se passe-t-il?</h5>
<p>Vous utilisez probablement un appareil ou un navigateur qui n'est pas recommandé pour l'utilisation de la plateforme <i>RSG en ligne</i>.<br>Il est aussi possible que la vitesse de votre connexion Internet soit insuffisante.</p>
<p>Afin d’optimiser votre expérience sur <i>RSG en ligne</i>, l’utilisation du navigateur <b>Google Chrome</b> et de l’Internet <b>haute vitesse</b> est recommandée.</p>
<p>Veuillez consulter la page <a href="/mod/rsg/requis/"><u>Exigences techniques</u></a> pour connaitre toutes les caractéristiques techniques nécessaires.</p>
<hr />
<div id="soutienQ03"></div>
<h5 class="static-page-title">L'écran des vidéos est trop petit. Que puis-je faire?</h5>
<p>Consultez l'Aide en ligne, située dans le haut de la page de la capsule. Des captures d'écran vous indiqueront de façon précise comment afficher la vidéo en mode plein écran.</p>
<hr />
<div id="soutienQ04"></div>
<h5 class="static-page-title">Le son des audios ou des vidéos est trop faible. Que puis-je faire?</h5>
<p>Consultez l'Aide en ligne, située dans le haut de la page de la capsule. Des captures d'écran vous indiqueront de façon précise comment régler le son de votre appareil ou de la capsule.</p>
<hr />
<div id="soutienQ05"></div>
<h5 class="static-page-title">Je ne vois pas tout le texte à l'écran. Que puis-je faire?</h5>
<p>Vous pouvez :
<ul>
    <li>utiliser la barre de défilement pour faire défiler la page avec votre souris lorsque la hauteur ou la largeur de la zone sont insuffisantes pour afficher l'intégralité du contenu; ou</li>
	<li>ajuster l'affichage de votre écran à 100%.</li>
</ul>
</p>
<hr />
<div id="soutienQ06"></div>
<h5 class="static-page-title">Qu'est-ce que la mémoire cache et pourquoi dois-je la vider lorsque je ferme ma session?</h5>
<p>La mémoire cache est l'endroit où le processeur de votre appareil enregistre les informations dont il a fréquemment besoin.<br>Vider la mémoire cache de votre navigateur :
<ul>
    <li>vous évite d'utiliser des anciens formulaires;</li>
    <li>protège vos renseignements personnels;</li>
    <li>facilite le bon fonctionnement de <i>RSG en ligne</i> sur votre ordinateur.</li>
</ul>
</p>
<hr />
<div id="soutienQ07"></div>
<h5 class="static-page-title">Quelqu’un peut-il m’aider si je rencontre un problème technique?</h5>
<p>Communiquez avec le soutien technique en utilisant le formulaire disponible dans la page <a href="/mod/rsg/contact/"><u>Nous joindre</u></a>. Prenez soin de décrire le problème rencontré de manière détaillée afin que nous puissions vous offrir un soutien approprié.
Si vous ne recevez pas de réponse dans un délai de <br />2 jours ouvrables, vérifiez votre boite de courrier indésirable.</p>

