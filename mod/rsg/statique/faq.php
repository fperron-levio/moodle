<div id="audio"></div></br>
<h3>Matériel informatique et audio</h3>
<hr />
<div id="audioQ01"></div>
<h5 class="static-page-title">Puis-je utiliser ma tablette pour suivre la formation?</h5>
<p>Oui, si votre tablette mesure 10 pouces ou plus sur la diagonale. Nous vous suggérons cependant d'utiliser un ordinateur de table ou un portable pour profiter pleinement des fonctionnalités de <i>RSG en ligne</i>.</p>
<hr />
<div id="audioQ02"></div>
<h5 class="static-page-title">Puis-je commencer une capsule sur ma tablette pour ensuite la terminer sur un ordinateur de table ou un portable?</h5>
<p>Oui. Vous pouvez changer d'appareil mais par mesure de sécurité, il est préférable de vous déconnecter d'abord de RSG en ligne sur la tablette pour vous connecter ensuite sur l'appareil avec lequel vous souhaitez terminer la capsule.</p>

<hr />
<div id="perfectionnement"></div></br>
<h3>Obligation de perfectionnement</h3>
<hr />
<div id="perfectionnementQ01"></div>
<h5 class="static-page-title">Ce perfectionnement peut-il être comptabilisé dans les six heures de perfectionnement obligatoire par année?</h5>
<p>Oui.<br>Selon l'article 59 du <a href="http://legisquebec.gouv.qc.ca/fr/showdoc/cr/S-4.1.1,%20r.%202" target="_blank"><u>Règlement</u></a> sur les services de garde éducatifs à l'enfance :<br>« La responsable doit suivre annuellement 6 heures d'activités de perfectionnement [...] dont au moins 3 heures portent sur le développement de l'enfant et le programme éducatif prévu par la Loi. »</p>
<hr />
<div id="perfectionnementQ02"></div>
<h5 class="static-page-title">Puis-je combiner la formation <i>RSG en ligne</i> à d'autres formations?</h5>
<p>Oui. Vous pouvez combiner les heures suivies en ligne avec les heures suivies ailleurs.</p>

